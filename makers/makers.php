<p>
What is Maker Fair?
<br>
Maker Fair is a platform to encourage all the people who like tinkering 

with their ideas. You can be a hobbyist or like playing with transformers 

or an avid coder or get your fingers dirty with paint, you are a Maker. 

This is the right platform for you to flaunt your skills.
</p>
<p>
2. What should I make?
<br>
You can make anything, be it a small toy or a big robot. There are no 

bounds for imagination.
</p>
<p>
3. How should I get ideas?
<br>
In the different themes that we have listed out here, select one theme 

you are really good at or interested in. If you and your team brainstorms 

for ideas, it's just a matter of time you can come up with a problem to 

solve. The problem can be anything. For example, redesigning a mosquito 

repellent, medical assistant robot, teaching aids for school children etc.
</p>
<p>
4. What type of ideas should I come up with?
<br>
You can come up with any type of idea which falls in one of the themes 

which we provided. Your product can be helpful to the police dept., fire 

dept., schools, sick patients etc.
</p>
<p>
5. What if I don't get ideas?
<br>
Unbounded enthusiasm and interest are all that we need. We are here to 

help you out with the other things. 

You can join a team irrespective of branch, batch and college. 

 You can contact our coordinators to brainstorm for ideas. 

 You can even take the organisers help and find out a team which 
</p>
<p>

6. Why should I participate in Maker Fair?
<br>
You can flaunt your skills before a lot of industrialists and renowned 

professors and educationalists. You will get to showcase your talent in 

front of a large audience for almost 2 days. The most important thing you 

will win is the satisfaction and the inner happiness you get when you see 

something which was in your head all this while become real. Your product 

may be featured in the newspaper article and Samgatha website if it 

really catches the attention of many people. Goodies and prizes to the 

teams with top 10 products will be given.

suits your interest.
