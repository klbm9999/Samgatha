ROUND III ( JunkYard Warz ) :<br><br>
The real task is up here now !!!!<br>
Selected teams will be provided with some junk. <br>They have to complete the given task with the junk within the given time. <br>There would be a common problem statement for which respective teams (who got selected after second round) have to provide a suitable solution with the materials being provided. <br>Prizes would be distributed based on how well the prototypes satisfies the requirements of the problem.<br>


